#include <iostream>     // Бібліотека для введення та виведення
#include <vector>       // Бібліотека для використання динамічних масивів (векторів)

using namespace std;

// Функція для обміну першого і останнього рядків у квадратній матриці
void swapFirstLastRows(vector<vector<int>>& matrix) {
    int n = matrix.size();  // Розмір матриці (кількість рядків або стовпців, оскільки матриця квадратна)
    for (int i = 0; i < n; i++) {
        swap(matrix[0][i], matrix[n-1][i]);  // Міняємо місцями відповідні елементи першого та останнього рядків
    }
}

// Функція для обчислення добутку елементів на головній діагоналі матриці
int productOfDiagonal(const vector<vector<int>>& matrix) {
    int n = matrix.size();   // Розмір матриці
    int product = 1;         // Змінна для збереження результату добутку
    for (int i = 0; i < n; i++) {
        product *= matrix[i][i];  // Множимо елементи, що знаходяться на головній діагоналі
    }
    return product;          // Повертаємо результат добутку
}

int main() {
    int n;

    // Отримуємо розмір квадратної матриці від користувача
    cout << "Введіть розмір квадратної матриці: ";
    cin >> n;

    // Ініціалізуємо квадратну матрицю розміром n x n
    vector<vector<int>> matrix(n, vector<int>(n));

    // Зчитуємо елементи матриці від користувача
    cout << "Введіть елементи матриці:" << endl;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cin >> matrix[i][j];  // Вводимо кожен елемент матриці
        }
    }

    // Виводимо початкову матрицю
    cout << "Початкова матриця:" << endl;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cout << matrix[i][j] << " ";  // Виводимо елемент матриці з пробілом
        }
        cout << endl;  // Перехід на новий рядок після кожного рядка матриці
    }

    // Виконуємо обмін першого та останнього рядків
    swapFirstLastRows(matrix);

    // Виводимо матрицю після обміну рядків
    cout << "Матриця після зміни першого і останнього рядків:" << endl;
    for (int i = 0; i < n; i++) {
        for (int j = 0; j < n; j++) {
            cout << matrix[i][j] << " ";  // Виводимо елемент оновленої матриці з пробілом
        }
        cout << endl;  // Перехід на новий рядок
    }

    // Обчислюємо добуток елементів на головній діагоналі
    int product = productOfDiagonal(matrix);
    cout << "Добуток елементів головної діагоналі: " << product << endl;

    return 0;  // Завершення програми
}
